import { describe, it, expect } from 'vitest';
import {
  MAX_RECENT_ITEMS,
  COINS_PER_CURRENCY_UNIT,
  COIN_TO_CURRENCY_RATE,
  COINS_REDEMPTION_THRESHOLD,
  STREAK_BONUS_THRESHOLD,
  STREAK_BONUS_MULTIPLIER,
  CREDIT_FEE_RATE,
  REFERRAL_BONUS_FREELANCER,
  REFERRAL_BONUS_EMPLOYER,
  ANALYTICS_PREMIUM_PRICE,
  DELIVERY_DAYS,
  DELIVERY_DAYS_MS,
} from '../constants';

describe('constants', () => {
  it.each([
    ['MAX_RECENT_ITEMS', MAX_RECENT_ITEMS],
    ['COINS_PER_CURRENCY_UNIT', COINS_PER_CURRENCY_UNIT],
    ['COIN_TO_CURRENCY_RATE', COIN_TO_CURRENCY_RATE],
    ['COINS_REDEMPTION_THRESHOLD', COINS_REDEMPTION_THRESHOLD],
    ['STREAK_BONUS_THRESHOLD', STREAK_BONUS_THRESHOLD],
    ['STREAK_BONUS_MULTIPLIER', STREAK_BONUS_MULTIPLIER],
    ['CREDIT_FEE_RATE', CREDIT_FEE_RATE],
    ['REFERRAL_BONUS_FREELANCER', REFERRAL_BONUS_FREELANCER],
    ['REFERRAL_BONUS_EMPLOYER', REFERRAL_BONUS_EMPLOYER],
    ['ANALYTICS_PREMIUM_PRICE', ANALYTICS_PREMIUM_PRICE],
    ['DELIVERY_DAYS', DELIVERY_DAYS],
    ['DELIVERY_DAYS_MS', DELIVERY_DAYS_MS],
  ])('%s should be a number', (_name, value) => {
    expect(typeof value).toBe('number');
  });

  it.each([
    ['MAX_RECENT_ITEMS', MAX_RECENT_ITEMS],
    ['COINS_PER_CURRENCY_UNIT', COINS_PER_CURRENCY_UNIT],
    ['COIN_TO_CURRENCY_RATE', COIN_TO_CURRENCY_RATE],
    ['COINS_REDEMPTION_THRESHOLD', COINS_REDEMPTION_THRESHOLD],
    ['STREAK_BONUS_THRESHOLD', STREAK_BONUS_THRESHOLD],
    ['STREAK_BONUS_MULTIPLIER', STREAK_BONUS_MULTIPLIER],
    ['CREDIT_FEE_RATE', CREDIT_FEE_RATE],
    ['REFERRAL_BONUS_FREELANCER', REFERRAL_BONUS_FREELANCER],
    ['REFERRAL_BONUS_EMPLOYER', REFERRAL_BONUS_EMPLOYER],
    ['ANALYTICS_PREMIUM_PRICE', ANALYTICS_PREMIUM_PRICE],
    ['DELIVERY_DAYS', DELIVERY_DAYS],
    ['DELIVERY_DAYS_MS', DELIVERY_DAYS_MS],
  ])('%s should be positive', (_name, value) => {
    expect(value).toBeGreaterThan(0);
  });

  it('DELIVERY_DAYS_MS should equal DELIVERY_DAYS * 24 * 60 * 60 * 1000', () => {
    expect(DELIVERY_DAYS_MS).toBe(DELIVERY_DAYS * 24 * 60 * 60 * 1000);
  });

  it('STREAK_BONUS_MULTIPLIER should be greater than 1', () => {
    expect(STREAK_BONUS_MULTIPLIER).toBeGreaterThan(1);
  });

  it('COIN_TO_CURRENCY_RATE should be between 0 and 1', () => {
    expect(COIN_TO_CURRENCY_RATE).toBeGreaterThan(0);
    expect(COIN_TO_CURRENCY_RATE).toBeLessThan(1);
  });
});
